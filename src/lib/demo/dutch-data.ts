// Helper functions
export function getRandomItem<T>(array: T[]): T {
  return array[Math.floor(Math.random() * array.length)]
}

export function getRandomItems<T>(array: T[], count: number): T[] {
  const shuffled = [...array].sort(() => 0.5 - Math.random())
  return shuffled.slice(0, count)
}

export function generateDutchPhoneNumber(): string {
  const prefixes = ['06', '06', '06', '020', '010', '030', '040', '050']
  const prefix = getRandomItem(prefixes)
  if (prefix === '06') {
    return `+316${Math.floor(10000000 + Math.random() * 90000000)}`
  }
  return `+31${prefix.slice(1)}${Math.floor(1000000 + Math.random() * 9000000)}`
}

export function generateDutchEmail(firstName: string, lastName: string, company?: string): string {
  const cleanFirst = firstName.toLowerCase().replace(/\s/g, '')
  const cleanLast = lastName.toLowerCase().replace(/\s/g, '').replace(/'/g, '')
  const domains = ['gmail.com', 'outlook.nl', 'hotmail.com', 'live.nl', 'ziggo.nl', 'kpnmail.nl', 'xs4all.nl']

  if (company && Math.random() > 0.5) {
    const companyDomain = company.toLowerCase()
      .replace(/[^a-z0-9]/g, '')
      .slice(0, 15) + '.nl'
    return `${cleanFirst}.${cleanLast}@${companyDomain}`
  }

  const patterns = [
    `${cleanFirst}.${cleanLast}`,
    `${cleanFirst}${cleanLast}`,
    `${cleanFirst[0]}${cleanLast}`,
    `${cleanFirst}${cleanLast[0]}`,
  ]

  return `${getRandomItem(patterns)}@${getRandomItem(domains)}`
}

export function generateDutchAddress(): { street: string; number: string; city: string; postal: string } {
  return {
    street: getRandomItem(DUTCH_STREETS),
    number: String(Math.floor(1 + Math.random() * 200)) + (Math.random() > 0.7 ? getRandomItem(['a', 'b', 'c', '']) : ''),
    city: getRandomItem(DUTCH_CITIES),
    postal: `${Math.floor(1000 + Math.random() * 9000)} ${String.fromCharCode(65 + Math.floor(Math.random() * 26))}${String.fromCharCode(65 + Math.floor(Math.random() * 26))}`,
  }
}

export function generateRandomDate(daysAgo: number, daysRange: number = 0): Date {
  const date = new Date()
  date.setDate(date.getDate() - daysAgo + Math.floor(Math.random() * daysRange))
  date.setHours(Math.floor(8 + Math.random() * 12)) // Business hours 8-20
  date.setMinutes(Math.floor(Math.random() * 60))
  return date
}
